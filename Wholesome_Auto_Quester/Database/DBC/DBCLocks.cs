        public static bool IsLockValid(int gotType, int lockId)
        {
            if (gotType != 3 || lockId <= 1)
            {
                return true;
            }

            if (LocksDic.TryGetValue(lockId, out DBCLock dbcLock))
            {
                if (dbcLock.LockProperties == 0)
                {
                    return true;
                }
                if (dbcLock.LockProperties == 1) // Lockpicking
                {
                    return Skill.GetValue(SkillLine.Lockpicking) > 0
                        && Skill.GetMaxValue(SkillLine.Lockpicking) >= dbcLock.RequiredSkill;
                }
                if (dbcLock.LockProperties == 2) // Herbalism
                {
                    return Skill.GetValue(SkillLine.Herbalism) > 0
                        && Skill.GetMaxValue(SkillLine.Herbalism) >= dbcLock.RequiredSkill;
                }
                if (dbcLock.LockProperties == 3) // Mining
                {
                    return Skill.GetValue(SkillLine.Mining) > 0
                        && Skill.GetMaxValue(SkillLine.Mining) >= dbcLock.RequiredSkill;
                }
                if (dbcLock.LockProperties == 20) // Inscription
                {
                    return Skill.GetValue(SkillLine.Inscription) > 0
                        && Skill.GetMaxValue(SkillLine.Inscription) >= dbcLock.RequiredSkill;
                }
                if (dbcLock.LockProperties > 21) // Item
                {
                    return ItemsManager.HasItemById((uint)dbcLock.LockProperties);
                }
            }
            else
            {
                throw new System.Exception($"Lock not found for id {lockId}");
            }
            return false;
        }
    }

